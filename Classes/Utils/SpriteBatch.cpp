SpriteBatch* SpriteBatch::create(const char* pTextureAtlas)
{
    SpriteBatch* entity = new SpriteBatch(pTextureAtlas);
    entity->initWithFile(pTextureAtlas, 10);
    entity->autorelease();
    
    return entity;
}

